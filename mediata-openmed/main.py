# Description helpers
# ---------------------------------------------------------------------------

def _sentence(text: str, fallback: str = "field") -> str:
    """Capitalise and ensure the text ends with a full stop."""
    s = (text or "").strip() or fallback.strip() or "field"
    if s.startswith('"') and s.endswith('"') and len(s) >= 2:
        s = s[1:-1].strip()
    s = s[:1].upper() + s[1:] if s else s
    if s and s[-1] not in ".!?":
        s += "."
    return s


def _build_value_phrase(
    v: str,
    col_label: str,
    scale_min: Optional[float],
    scale_max: Optional[float],
) -> str:
    """Build a contextual prompt for a **numeric** value to pass to the model.

    Only uses the word "score" when an explicit ordinal scale range (min/max) is
    known.  Without scale bounds the value could be a category code (e.g. 0=No,
    1=Yes), so a neutral phrasing is used instead.

    The phrase is intended as the model's **input prompt**.  The model processes
    it and generates the clinical description (e.g. "dependent", "independent").
    In fallback mode (no model) the normalised phrase itself is returned as the
    description.

    Examples
    --------
    col_label="Ability to use toilet", v="0", min=0, max=10
      → "score 0 of 10 measuring Ability to use toilet"

    col_label="Barthel index", v="50", min=0, max=100
      → "score 50 of 100 measuring Barthel index"

    col_label="Gender", v="1", no scale   (category code)
      → "1 as a value of Gender"
    """
    label = (col_label or "").strip()

    if scale_min is not None and scale_max is not None and scale_max > scale_min:
        max_str = str(int(scale_max)) if scale_max == int(scale_max) else str(scale_max)
        if label:
            return f"score {v} of {max_str} measuring {label}"
        return f"score {v} of {max_str}"

    # No scale bounds → may be a category code; use neutral phrasing
    if label:
        return f"{v} as a value of {label}"
    return f"value {v}"


def _build_category_phrase(v: str, col_label: str) -> str:
    """Build a contextual prompt for a **text/categorical** value to pass to the model.

    The phrase identifies the value as a category of the column concept so the
    model can infer the appropriate clinical description without any hardcoded
    term mappings.

    The phrase is intended as the model's **input prompt**.  In fallback mode
    the normalised phrase is used as the description.

    Examples
    --------
    col_label="Stroke", v="Ischemic"
      → "Ischemic as a category of Stroke"

    col_label="Gender", v="Male"
      → "Male as a category of Gender"
    """
    label = (col_label or "").strip()
    if label:
        return f"{v} as a category of {label}"
    return v


@app.post("/describe_batch", response_model=DescribeBatchResponse)
async def describe_batch(request: DescribeBatchRequest):
    """Generate plain-language descriptions for column headers and their values.

    For each ``ColumnDescInput``:

    - **col_desc**: SNOMED terminology label when available; otherwise normalise
      the column key.
    - **values[i].d**:
        1. If a SNOMED terminology label is provided for the value, use it directly.
        2. If the value is numeric **and** scale bounds (min/max) are known, build
           an ordinal score prompt and pass it to the generative LLM:
           ``"score {v} of {max} measuring {col_label}"``
        3. If the value is numeric **without** scale bounds (could be a category
           code), build a neutral value prompt and pass it to the generative LLM:
           ``"{v} as a value of {col_label}"``
        4. If the value is text/categorical, build a category prompt and pass it
           to the generative LLM: ``"{v} as a category of {col_label}"``

    In all cases the contextual phrase is the **generative LLM's input prompt**.
    The LLM produces the clinical description (e.g. "Low ability to use toilet",
    "Ischemic stroke", "Male gender").  When the generative model is unavailable
    the normalised prompt phrase is returned as a fallback description.
    """
    describe_pipe = _load_describe_model_once()
    t0 = time.time()
    logger.info("[OpenMed] /describe_batch – %d columns", len(request.columns))

    results: List[ColumnDesc] = []
    for col in request.columns:
        col_key = (col.col_key or "").strip()
        if not col_key:
            continue

        # Column description: extract plain label from SNOMED string, then normalise col_key
        term_label = _extract_label(col.terminology_label or "")
        col_desc_raw = term_label if term_label else _normalise(col_key)
        col_desc = _sentence(col_desc_raw, col_key)

        value_descs: List[ValueDesc] = []
        for val_info in (col.values or []):
            v = (val_info.v or "").strip()
            if not v:
                continue

            val_term = _extract_label(val_info.terminology_label or "")
            if val_term:
                # SNOMED label available – use it directly, no model call needed.
                d = _sentence(val_term, v)

            elif re.match(r"^-?\d+(\.\d+)?$", v):
                # Numeric value: build a contextual prompt and pass it to the
                # generative LLM.  Use "score" phrasing only when scale bounds
                # are known; without them the value may be a category code.
                scale_min: Optional[float] = None
                scale_max: Optional[float] = None
                try:
                    if val_info.min is not None:
                        scale_min = float(val_info.min)
                    if val_info.max is not None:
                        scale_max = float(val_info.max)
                except (ValueError, TypeError):
                    pass
                prompt = _build_value_phrase(v, col_desc_raw, scale_min, scale_max)
                generated = _generate_desc(prompt, describe_pipe)
                d = _sentence(generated if generated else prompt, v)

            else:
                # Text/categorical value: build a category prompt and pass it to
                # the generative LLM.
                prompt = _build_category_phrase(v, col_desc_raw)
                generated = _generate_desc(prompt, describe_pipe)
                d = _sentence(generated if generated else _normalise(v), v)

            value_descs.append(ValueDesc(v=v, d=d))

        results.append(ColumnDesc(
            col_key=col_key,
            col_desc=col_desc,
            values=value_descs,
        ))

    elapsed = time.time() - t0
    logger.info("[OpenMed] Described %d column(s) in %.2fs", len(results), elapsed)
    return DescribeBatchResponse(columns=results)
