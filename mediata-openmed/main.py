# Model management
# ---------------------------------------------------------------------------

# --- OpenMed NER model (used by /batch for SNOMED term extraction) ---
# OpenMed/OpenMed-NER-DiseaseDetect-SuperClinical-434M is a DeBERTa-based NER
# model trained on SuperClinical data to detect disease entities in biomedical
# text.  It extracts the clearest medical concept from a column name or value
# string so that Snowstorm can find the matching SNOMED CT concept.
NER_MODEL_NAME = "OpenMed/OpenMed-NER-DiseaseDetect-SuperClinical-434M"

# Minimum NER confidence score to accept an entity extraction.
# Below this threshold the service falls back to text normalisation.
MIN_CONFIDENCE_THRESHOLD: float = 0.50

_pipeline = None          # set to a callable on success, or "fallback" sentinel


def _load_model_once():
    """Load the HuggingFace NER pipeline (idempotent; thread-safe for the
    single startup call pattern FastAPI uses)."""
    global _pipeline
    if _pipeline is not None:
        return _pipeline

    try:
        from transformers import pipeline as hf_pipeline

        logger.info("[OpenMed] Loading biomedical NER model: %s", NER_MODEL_NAME)
        t0 = time.time()
        _pipeline = hf_pipeline(
            "ner",
            model=NER_MODEL_NAME,
            aggregation_strategy="first",   # "first" merges BPE sub-tokens into whole words
            device=-1,          # CPU only – avoids GPU dependency in tests
        )
        logger.info("[OpenMed] NER model loaded in %.1fs", time.time() - t0)

    except (ImportError, OSError, RuntimeError, EnvironmentError) as exc:
        logger.warning(
            "[OpenMed] Could not load NER model (%s: %s). "
            "Using text-normalisation fallback.",
            type(exc).__name__, exc,
        )
        _pipeline = "fallback"   # sentinel – means "use normalise() only"
    except Exception as exc:  # noqa: BLE001
        logger.warning(
            "[OpenMed] Unexpected error loading NER model (%s: %s). "
            "Using text-normalisation fallback.",
            type(exc).__name__, exc,
        )
        _pipeline = "fallback"

    return _pipeline


# --- Generative LLM (used by /describe_batch for description generation) ---
# OpenMeditron/Meditron3-Gemma2-2B is a Gemma2-based medical LLM fine-tuned on
# clinical data.  It produces precise clinical descriptions from contextual
# prompts, e.g.:
#   "score 0 of 10 measuring Ability to use toilet"  → "Unable to use toilet independently"
#   "score 10 of 10 measuring Ability to use toilet" → "Independent use of toilet"
#   "score 100 of 100 measuring Barthel index"       → "Full independence in ADL's"
DESCRIBE_MODEL_NAME = "OpenMeditron/Meditron3-Gemma2-2B"

_describe_pipeline = None   # set to (tokenizer, model) tuple on success, or "fallback"

# Meditron3-Gemma2-2B does not support a system role – instruction is embedded
# directly in the user turn.
_DESCRIBE_PROMPT_TEMPLATE = (
    'Clinical phrase: "{phrase}"\n'
    "Write a SHORT clinical description (2-6 words) for this phrase. "
    "Only output the description:"
)


def _load_describe_model_once():
    """Load the OpenMeditron generative LLM used to generate value descriptions.

    Returns a ``(tokenizer, model)`` tuple on success, or the string
    ``"fallback"`` when the model cannot be loaded.  The fallback path
    uses normalised phrase text as the description.
    """
    global _describe_pipeline
    if _describe_pipeline is not None:
        return _describe_pipeline

    try:
        import torch
        from transformers import AutoModelForCausalLM, AutoTokenizer

        logger.info("[OpenMed] Loading description model: %s", DESCRIBE_MODEL_NAME)
        t0 = time.time()
        tok = AutoTokenizer.from_pretrained(DESCRIBE_MODEL_NAME)
        mdl = AutoModelForCausalLM.from_pretrained(
            DESCRIBE_MODEL_NAME, dtype=torch.bfloat16
        )
        mdl.eval()
        _describe_pipeline = (tok, mdl)
        logger.info("[OpenMed] Description model loaded in %.1fs", time.time() - t0)

    except (ImportError, OSError, RuntimeError, EnvironmentError) as exc:
        logger.warning(
            "[OpenMed] Could not load description model (%s: %s). "
            "Using text-normalisation fallback.",
            type(exc).__name__, exc,
        )
        _describe_pipeline = "fallback"
    except Exception as exc:  # noqa: BLE001
        logger.warning(
            "[OpenMed] Unexpected error loading description model (%s: %s). "
            "Using text-normalisation fallback.",
            type(exc).__name__, exc,
        )
        _describe_pipeline = "fallback"

    return _describe_pipeline


def _generate_desc(prompt: str, pipe) -> str:
    """Generate a short clinical description from a contextual prompt.

    Uses OpenMeditron/Meditron3-Gemma2-2B, a medical LLM that produces precise
    clinical language from contextual phrases.

    Falls back to normalised prompt text when the model is unavailable.

    Examples (with model loaded)
    ----------------------------
    prompt="score 0 of 10 measuring Ability to use toilet"
      → "Unable to use toilet independently"

    prompt="score 10 of 10 measuring Ability to use toilet"
      → "Independent use of toilet"

    prompt="score 100 of 100 measuring Barthel index"
      → "Full independence in ADL's"

    prompt="Ischemic as a category of Stroke"
      → "Ischemic stroke"
    """
    if not prompt or not prompt.strip():
        return prompt or ""

    normalised = _normalise(prompt)

    if pipe is None or pipe == "fallback":
        return normalised

    try:
        import torch
        tok, mdl = pipe
        # Meditron3-Gemma2-2B does not support a system role; embed the
        # instruction directly in the user turn.
        user_content = _DESCRIBE_PROMPT_TEMPLATE.format(phrase=prompt.strip())
        msgs = [{"role": "user", "content": user_content}]
        text = tok.apply_chat_template(msgs, tokenize=False, add_generation_prompt=True)
        ids = tok([text], return_tensors="pt")
        with torch.no_grad():
            out = mdl.generate(**ids, max_new_tokens=20, do_sample=False)
        gen_ids = out[0][ids["input_ids"].shape[-1]:]
        result = tok.decode(gen_ids, skip_special_tokens=True).strip()
        # Trim to the first sentence/line.
        result = re.split(r"[\n.]", result)[0].strip()
        alpha_chars = sum(1 for c in result if c.isalpha())
        if result and alpha_chars >= 2:
            logger.debug("[OpenMed] Generated desc for '%s' → '%s'", prompt[:40], result)
            return result
    except Exception as exc:  # noqa: BLE001
        logger.warning("[OpenMed] Description generation error for '%s': %s", prompt[:40], exc)

    return normalised


